public class ifElse {

   public static void main(String args[]) {
      int a = 20;
      if (a == 10) {
         System.out.print("value of a is 10");
      } 
      else if (a == 20) {
         System.out.print("value of a is 20");
      }
      else if (a == 30) {
         System.out.print("value of a is 30");
      }
      else {
         System.out.print("this is else statement");
      }

   }
}
